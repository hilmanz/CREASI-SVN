                            <h3><a href="index.php?page=news-detail">Wajah Jokowi di Majalah "Time" 
Bikin Heboh Medsos</a></h3>
                            <div class="meta-post">
                                <span class="author">by: www.creasi.co.id</span>
                            </div>
                        </div><!-- end .entry-title -->
                        <div class="entry-detail">
                            <p>KOMPAS.com - Majalah Time bakal memuat foto presiden terpilih Joko Widodo sebagai sampul (cover) dengan judul headline berbunyi, "A New Hope" atau "Sebuah Harapan Baru".</p>

<p>Hal tersebut tak pelak menimbulkan kehebohan di kalangan pengguna media sosial Indonesia. Banyak orang yang sibuk berbagi gambar sampul berisi foto mantan Wali Kota Solo yang akrab disapa Jokowi itu. Berbagai komentar di Twitter, Facebook, dan Path pun ikut terlontar.</p>

<p>"Cover TIME minggu ini, Jokowi: A New Hope. Cover TIME minggu depan, Prabowo: The Empire Strikes Back," demikian cuit seorang pengguna Twitter bernama @kamentrader, mengacu pada pemilihan headline yang mirip dengan judul film pertama pada trilogi Star Wars Original.</p>

<p>Sutradara film, Joko Anwar, pun ikut menuliskan cuitan di akun Twitter-nya, "Presiden kita yang ketujuh. I belive in him," sambil menyertakan foto cover Time edisi Jokowi.</p>

<p>Beberapa netizen ada yang secara khusus menanggapi wajah Jokowi. "Aku kok merinding ya lihat Jokowi di sampul muka majalah TIME," cuit Aisyah Hilal. Ada juga yang mencuitkan senang dengan foto Jokowi tersebut karena menampilkan sosok Jokowi yang tulus dan jujur.</p> 

<p>Di Facebook, seorang pengguna mengomentari wajah Jokowi terlihat lebih tua dan kelihatan tambah banyak rambut putihnya ketimbang saat menjadi gubernur DKI. Sementara itu, di Path, seorang pengguna membagikan cover Time ini sambil menulis, "Harapan bagi orang tampang ndeso yang bercita2 jadi Presiden."</p>

<p>Di Facebook, seorang pengguna mengomentari wajah Jokowi terlihat lebih tua dan kelihatan tambah banyak rambut putihnya ketimbang saat menjadi gubernur DKI. Sementara itu, di Path, seorang pengguna membagikan cover Time ini sambil menulis, "Harapan bagi orang tampang ndeso yang bercita2 jadi Presiden."</p>

<p>Cover Time dimaksud memang menampilkan close-up wajah Jokowi. Dalam foto jepretan Adam Ferguson itu, tatapan mata Jokowi yang lurus ke arah depan dan raut muka yang dibuat agak kaku menyiratkan ketegasan.</p>

<p>"Negara ini baru saja memilih presiden baru, silakan berkenalan dengan Joko Widodo."</p>
